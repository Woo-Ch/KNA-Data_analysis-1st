# 실습1. 사칙연산 계산기, 기초

a = 17
b = 5

print(a + b) # 22
print(a - b) # 12
print(a * b) # 85
print(a / b) # 3.4
print(a // b) # 3
print(a % b) # 2
print(a ** b) # 1419857


